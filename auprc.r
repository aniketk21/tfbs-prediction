# order x in increasing fashion
x <- c(0.5106817614770459, 0.5183289670658683, 0.5373939620758483, 0.5374968812375249, 0.5375, 0.6078125, 0.671875, 1.0)
y <- c(0.8747061965811965, 0.8726954804900205, 0.8407826680979799, 0.8387622704686261, 0.837259464737688,0.7941363432622958, 0.75331496349344, 0.3132182925578419)
